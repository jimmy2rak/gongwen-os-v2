/** 根据公文类型获取内置模板 */
export function getBuiltinTemplate(category: string): BuiltinTemplate | undefined {
  return BUILTIN_TEMPLATES.find((t) => t.category === category);
}

/** 获取所有内置模板的分类 Skill 定义 */
export function getBuiltinSkillsForCategory(category: string): BuiltinSkillDef[] {
  return getBuiltinTemplate(category)?.builtinSkills ?? [];
}

/** 获取全部内置 Skill（用于初始化 seed） */
export function getAllBuiltinSkills(): Array<{ category: string; skill: BuiltinSkillDef }> {
  const result: Array<{ category: string; skill: BuiltinSkillDef }> = [];
  for (const t of BUILTIN_TEMPLATES) {
    for (const s of t.builtinSkills) {
      result.push({ category: t.category, skill: s });
    }
  }
  return result;
}
