// ─── 内置公文模板 ───────────────────────────────
// 11 种法定/常用公文类型，每种含：标题模式、必备章节、结构提示、格式规范。
// content 为 JSON 字符串，供 template-to-doc.ts 解析为编辑器初稿 HTML。
// 同时每种模板关联内置默认 Skill（写作规范），存于 BUILTIN_SKILLS。

import { DOCUMENT_CATEGORIES } from "@/types";

// ── 模板内容结构 ──

export interface GovDocTemplateContent {
  /** 标题模板，{topic} 为用户填写的主题 */
  titlePattern: string;
  /** 必备章节标题列表（按顺序） */
  sections: string[];
  /** 各章节对应的默认示例内容（留空则只显示章节标题） */
  sectionSamples: string[];
  /** 结构提示词（给 AI 的生成指引） */
  structureHint: string;
  /** GB/T 9704-2012 该类公文的特殊格式要求 */
  formatRules: string[];
}

export interface BuiltinTemplate {
  id: string;            // "builtin-{category}"
  name: string;          // 显示名称
  category: string;      // 对应 DOCUMENT_CATEGORIES 之一
  type: "builtin";
  description: string;   // 简要说明
  content: string;       // JSON.stringify(GovDocTemplateContent)
  /** 该类公文内置默认 Skill 列表 */
  builtinSkills: BuiltinSkillDef[];
}

export interface BuiltinSkillDef {
  name: string;
  content: string;       // 写作规范文本（注入 AI system prompt）
}

// ── 11 类公文默认模板定义 ──

function tpl(category: string, opts: {
  name: string;
  titlePattern: string;
  sections: string[];
  sectionSamples: string[];
  structureHint: string;
  formatRules: string[];
  skills: { name: string; content: string }[];
}): BuiltinTemplate {
  const content: GovDocTemplateContent = {
    titlePattern: opts.titlePattern,
    sections: opts.sections,
    sectionSamples: opts.sectionSamples,
    structureHint: opts.structureHint,
    formatRules: opts.formatRules,
  };
  return {
    id: `builtin-${category}`,
    name: opts.name,
    category,
    type: "builtin",
    description: `${category}类公文标准框架模板`,
    content: JSON.stringify(content),
    builtinSkills: opts.skills,
  };
}

